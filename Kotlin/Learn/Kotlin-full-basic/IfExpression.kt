fun main(){
    // If Expression
    var nilai : Int = 85

    if(nilai > 90){
        println("Nilai Anda A")
    }
    else if(nilai > 80){
        println("Nilai Anda B")
    }
    else if(nilai > 70){
        println("Nilai anda C")
    }
    else if(nilai > 60){
        println("Nilai Anda D")
    }
    else{
        println("Nilai Anda E, dan harap ikut Remedial")
    }

}